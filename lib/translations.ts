export function translateCostLabel(label: string, locale: Locale = "ka"): string {
  if (locale === "ka") return label;
  if (COST_LABELS_EN[label]) return COST_LABELS_EN[label];
  // Dynamic VAT label: "დღგ (18%)" -> "VAT (18%)"
  const vatMatch = label.match(/^დღგ\s*\(([^)]+)\)$/);
  if (vatMatch) return `VAT (${vatMatch[1]})`;
  return label;
}

// Hints contain interpolated numbers/USD amounts, so we translate by
// replacing the Georgian fragments while leaving numbers intact.
const HINT_FRAGMENTS: Array<[RegExp, string]> = [
  [/მყიდველის, ბიდის, გეითის მოსაკრებლები/g, "buyer, bid & gate fees"],
  [/ელექტრომობილი - გათავისუფლებულია/g, "Electric vehicle - exempt"],
  [/CIF ღირებულება \+ აქციზი/g, "CIF value + excise"],
  [/ასაკი\s*(\d+)\s*წ\./g, "age $1 yr"],
  [/×\s*([\d.]+)\s*ჰიბრიდი/g, "× $1 hybrid"],
  [/×\s*3\s*მარჯვენა საჭე/g, "× 3 right-hand drive"],
  [/სმ³/g, "cm³"],
  [/₾/g, "GEL"],
];

export function translateCostHint(hint: string | undefined, locale: Locale = "ka"): string | undefined {
  if (!hint) return hint;
  if (locale === "ka") return hint;
  let out = hint;
  for (const [pattern, replacement] of HINT_FRAGMENTS) {
    out = out.replace(pattern, replacement);
  }
  return out;
}

// Transport label: "აშშ - სტანდარტული ტარიფი" / "აშშ-იდან ფოთამდე"
// Normalize any em/en dash to a plain hyphen first, so both the match and
// the Georgian display use "-" (stored snapshots may contain "—").
export function translateTransportLabel(label: string, locale: Locale = "ka"): string {
  const normalized = label.replace(/[\u2012\u2013\u2014\u2015\u2212]/g, "-");
  if (locale === "ka") return normalized;
  if (normalized === "აშშ - სტანდარტული ტარიფი") return "USA - Standard Rate";
  if (normalized === "აშშ-იდან ფოთამდე") return "From USA to Poti";
  return normalized;
}

